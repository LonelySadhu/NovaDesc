    echo ""
    echo "  ███╗   ██╗ ██████╗ ██╗   ██╗ █████╗ ██████╗ ███████╗███████╗ ██████╗"
    echo "  ████╗  ██║██╔═══██╗██║   ██║██╔══██╗██╔══██╗██╔════╝██╔════╝██╔════╝"
    echo "  ██╔██╗ ██║██║   ██║██║   ██║███████║██║  ██║█████╗  ███████╗██║"
    echo "  ██║╚██╗██║██║   ██║╚██╗ ██╔╝██╔══██║██║  ██║██╔══╝  ╚════██║██║"
    echo "  ██║ ╚████║╚██████╔╝ ╚████╔╝ ██║  ██║██████╔╝███████╗███████║╚██████╗"
    echo "  ╚═╝  ╚═══╝ ╚═════╝   ╚═══╝  ╚═╝  ╚═╝╚═════╝ ╚══════╝╚══════╝ ╚═════╝"
    echo "  Maintenance & Repair Management System v${VERSION}"
    echo ""

    check_docker
    configure

    cp "$(dirname "$0")/../docker-compose.yml" "$COMPOSE_FILE"

    pull_images
    start
